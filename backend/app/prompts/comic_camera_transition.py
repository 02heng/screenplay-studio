"""AI 漫剧 / 竖屏剧：运镜与转场提示词参考库（用户整理，供分镜与剪辑阶段引用）。

在分镜 JSON 中：请在 camera_movement、animation_prompt、director_note 中写清所选运镜/转场；
在剪辑 JSON 的 timeline 中：请在 transition_in / transition_out / vfx / director_note 中写清转场类型与执行指令。
"""

from __future__ import annotations


def storyboard_camera_addon() -> str:
    """供分镜阶段 system prompt 追加。"""
    return """

## 【运镜与转场参考库】（写入 animation_prompt 与 director_note，须与剧情情绪匹配）

### 一、平移类（上下左右移动）
1. 左滑穿越镜：从左向右平移拍摄，制造空间转场感。
2. 右滑对话镜：右向左平移，适合展示人物互动。
3. 上升视角拉伸：镜头向上拉动，模拟视野变高。
4. 下降推进：镜头缓慢下降，营造稳重/沉思氛围。
5. 横扫全景：平移横扫场景，适合城市风/展台。
6. 交错对撞滑：两组镜头反向滑动，用于对比画面。
7. 左右镜像切换：人物朝相反方向走出/进入镜头。

### 二、推拉类（变焦、推进、拉远）
1. 快速推进：快速推近主体，制造视觉冲击。
2. 缓慢拉远：缓慢远离画面，营造空镜或思绪感。
3. 特写推入：镜头缓慢靠近局部，突出细节。
4. 拉远转场：拉远同时转场到下个场景。
5. 拉焦突变：突然拉远营造「跳出」效果。
6. 推镜穿梭：镜头推进后无缝衔接下一个地点。
7. 呼吸式拉近拉远：镜头轻推轻拉，如呼吸节奏。

### 三、旋转类（旋转、翻转、转圈）
1. 上旋进场：从下向上旋转进入画面。
2. 下旋出场：镜头旋转下沉后切换场景。
3. 人物环绕360：镜头围绕人物一圈。
4. 倾斜旋转切换：画面倾斜+旋转后进入新场景。
5. 螺旋转场：画面旋转模糊切至下个地点。
6. 左右摇镜：轻微左右旋转，制造不稳感。

### 四、跟拍/绕拍类（人物动态）
1. 背后跟拍进场：从人物背后跟拍进入场景。
2. 侧面并行：与人物同行，适合走路戏。
3. 迎面走来：镜头后退拍摄迎面走来的人物。
4. 环绕三分：镜头绕人物约四分之三周后切入关键特写。
5. 走位转场：人物走过镜头完成自然转场。
6. 人物绕景：镜头随人物绕一圈展示环境。
7. 背影推进：拍背影，镜头逐步推进。

### 五、创意特效类（多镜结合、节奏感）
1. 手遮镜转场：手遮镜头+剪辑无缝衔接。
2. 甩头甩镜：镜头随甩头动作快速衔接转场。
3. 甩衣切换：甩外套或快速转身触发剪辑切幕。
4. 手机遮镜切换：用手机遮挡镜头后切到新场景。
5. 快闪连拍：多组快切画面，打出节奏冲击。
6. 心跳节奏运镜：镜头与 BGM 中的心跳节奏同期振动。
7. 眨眼转场：画面暗下模拟闭眼，再切新场景。
8. 遮挡转场：利用柱子、墙体等遮挡画面转入新空间。

### 六、情绪/情感类（氛围为主）
1. 慢推情绪镜：慢推近人物面部，传达情感。
2. 光影穿透：镜头穿越窗帘或百叶窗制造情绪感。
3. 虚实切换：前景虚焦+后景实拍制造层次。
4. 静态长镜头：一个镜头定格情绪，全程无切换。
5. 焦点漂移：镜头在场景中自动游移找焦点。
6. 空镜扫景：拍摄空房、物件，增强氛围铺垫。

### 七、节奏类（快慢节奏变化）
1. 快慢交替镜：快节奏切镜+慢节奏推进。
2. 音乐节奏动镜：镜头动作与音乐节奏同步。
3. 慢动作延展：运镜中加入轻微慢动作特效。
4. 跳剪时间流：通过跳剪表达时间流动感。
5. 倒放视角：镜头倒放，制造穿越/回忆感。
6. 闪切节奏感：镜头快速切换打出节奏冲击。

### 八、组合玩法（复合式运镜）
1. 平移+旋转：镜头一边平移一边旋转。
2. 推进+手遮切镜：推进后手遮画面切入新场景。
3. 环绕+对焦变化：围绕人物转的同时调整对焦。

### 九、转场特效（剪辑/VFX 可共用）
1. 闪白过渡｜回忆/梦境/爆炸冲击：画面瞬间变白约 0.3 秒，渐显新场景，模拟闪回或眩晕。
2. 闪黑转场｜时间跳跃/章末：快速变黑约 0.5 秒，淡入下一幕，适合段落分隔或压迫感。
3. 叠化溶解｜时间流逝/情绪过渡：上镜渐隐下镜渐现，重叠约 1 秒，表现时光或思绪。
4. 划像扫过｜空间转换：光带或硬边从左向右扫过，扫过区变为新场景。
5. 旋涡扭曲｜梦境/幻觉：画面绕心扭曲成旋涡，消散后接新场。
6. 推拉穿梭｜穿越/疾行：急速前推+边缘速度线，短暂模糊后弹出新场景。
7. 快门开合｜定格/暂停：上下黑边向中间合拢全黑再打开，接新画面。
8. 颜色溶化｜情绪/季节：画面褪为单色再注入新色调。
9. 玻璃碎裂｜冲突爆发：裂纹碎开飞散，露出后方新场景。
10. 水墨晕染｜古风诗意：边缘如墨晕染铺满后褪去，显现下一画面。
11. 光圈缩放｜聚焦真相：中心圆形光圈扩大，圈内新场景圈外压暗。
12. 抖动位移｜冲击混乱：快速上下/左右抖+轻模糊，停时已换场景。

使用要求：每集至少 2–3 处明确写出「运镜名称+一句话执行指令」；关键情绪转折或时空跳转处必须选用对应转场并写清时长体感。"""



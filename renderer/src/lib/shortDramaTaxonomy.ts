const defaultMeta = (): ShortDramaMeta => ({
  audience: 'female',
  primary: '现代言情',
  secondary: '总裁豪门',
});

export function loadShortDramaMeta(): ShortDramaMeta {
  try {
    const raw = localStorage.getItem(SHORT_DRAMA_META_STORAGE_KEY);
    if (!raw) return defaultMeta();
    const o = JSON.parse(raw) as Partial<ShortDramaMeta>;
    const audience = o.audience === 'male' || o.audience === 'female' ? o.audience : 'female';
    const base = defaultMeta();
    const plist = [...getPrimaryOptions(audience)];
    let primary =
      typeof o.primary === 'string' && o.primary && plist.includes(o.primary)
        ? o.primary
        : (plist[0] ?? base.primary);
    let secondary =
      typeof o.secondary === 'string' && o.secondary ? o.secondary : base.secondary;
    const secOpts = getSecondaryOptions(primary);
    if (!secOpts.includes(secondary)) secondary = secOpts[0] ?? '不限';

    return { audience, primary, secondary };
  } catch {
    return defaultMeta();
  }
}

export function saveShortDramaMeta(meta: ShortDramaMeta): void {
  localStorage.setItem(SHORT_DRAMA_META_STORAGE_KEY, JSON.stringify(meta));
}

export function loadShortDramaTagSet(): Set<string> {
  try {
    const raw = localStorage.getItem(SHORT_DRAMA_TYPES_STORAGE_KEY);
    if (!raw) return new Set();
    const arr = JSON.parse(raw) as unknown;
    if (!Array.isArray(arr)) return new Set();
    return new Set(arr.filter((x): x is string => typeof x === 'string'));
  } catch {
    return new Set();
  }
}

export function getPrimaryOptions(audience: ShortDramaAudience): readonly string[] {
  return audience === 'male' ? MALE_PRIMARY_TYPES : FEMALE_PRIMARY_TYPES;
}

export function getSecondaryOptions(primary: string): string[] {
  const raw = SECONDARY_BY_PRIMARY[primary];
  if (raw && raw.length > 0) return [...raw];
  return ['不限'];
}

/** 拼成发给后端的 drama_types 列表（有序、去重） */
export function buildShortDramaTypesPayload(
  meta: ShortDramaMeta,
  tagSet: Set<string>,
): string[] {
  const head = [
    `受众:${meta.audience === 'male' ? '男生' : '女生'}`,
    `一级:${meta.primary}`,
    `二级:${meta.secondary}`,
  ];
  const tags = [...tagSet].map((t) => t.trim()).filter(Boolean);
  const seen = new Set<string>();
  const out: string[] = [];
  for (const x of [...head, ...tags]) {
    if (seen.has(x)) continue;
    seen.add(x);
    out.push(x);
  }
  return out;
}